const fs = require('fs');
const { Octokit } = require('@octokit/rest');

async function main() {
  const octokit = new Octokit({ auth: process.env.GITHUB_TOKEN });
  const [owner, repo] = process.env.REPO.split('/');
  const prNumber = parseInt(process.env.PR_NUMBER, 10);

  // Read the ReviewResult from core CLI
  const result = JSON.parse(fs.readFileSync(process.env.RESULT_FILE, 'utf8'));

  // Format for GitHub
  const comment = formatForGitHub(result);

  // Post to PR
  await octokit.issues.createComment({
    owner,
    repo,
    issue_number: prNumber,
    body: comment
  });

  console.log('Review posted successfully');
}

function formatForGitHub(result) {
  // Verdict mapping per TEMPLATE.md
  const verdicts = {
    approve: { icon: 'вњ…', text: '**LGTM** вЂ” approve and merge' },
    request_changes: { icon: 'вљ пёЏ', text: '**Changes requested** вЂ” do not merge' },
    comment: { icon: 'вЏёпёЏ', text: '**Hold** вЂ” let\'s discuss the approach' }
  };

  const v = verdicts[result.recommendation] || verdicts.comment;
  let body = `${v.icon} ${v.text}\n\n`;

  // Summary (required when not approved)
  if (result.recommendation !== 'approve' && result.summary) {
    body += `${result.summary}\n\n`;
  }

  // Group findings
  const findings = result.findings || [];
  const critical = findings.filter(f => f.severity === 'critical');
  const warnings = findings.filter(f => f.severity === 'warning');
  const questions = findings.filter(f => f.type === 'question');
  const suggestions = findings.filter(f => f.severity === 'info' && f.type === 'suggestion');

  // Must Fix
  if (critical.length > 0) {
    body += 'рџ”ґ Must fix\n\n';
    critical.forEach((f, i) => {
      body += `${i + 1}. **${f.title}**`;
      if (f.file) {
        body += ` вЂ” \`${f.file}${f.line ? ':' + f.line : ''}\``;
      }
      body += '\n';
    });
    body += '\n';
  }

  // Should Fix
  if (warnings.length > 0) {
    body += 'рџџЎ Should fix\n\n';
    warnings.forEach(f => {
      body += `- **${f.title}**`;
      if (f.file) {
        body += ` вЂ” \`${f.file}${f.line ? ':' + f.line : ''}\``;
      }
      body += '\n';
    });
    body += '\n';
  }

  // Questions
  if (questions.length > 0) {
    body += 'рџ’¬ Questions for the team\n\n';
    questions.forEach(f => {
      body += `- **${f.title}**\n`;
    });
    body += '\n';
  }

  // Suggestions (collapsed by default)
  if (suggestions.length > 0) {
    body += '<details>\n<summary>рџ’Ў Suggestions (non-blocking)</summary>\n\n';
    suggestions.forEach(f => {
      body += `- ${f.title}`;
      if (f.file) {
        body += ` вЂ” \`${f.file}${f.line ? ':' + f.line : ''}\``;
      }
      body += '\n';
    });
    body += '</details>\n\n';
  }

  // Footer
  body += '---\n\n*Generated by Open Review*';

  return body;
}

main().catch(err => {
  console.error('Failed to post review:', err);
  process.exit(1);
});
