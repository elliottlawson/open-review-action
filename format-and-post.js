const fs = require('fs');
const { Octokit } = require('@octokit/rest');

// Marker to identify Open Review comments
const REVIEW_MARKER = '*Generated by Open Review*';

async function main() {
  const octokit = new Octokit({ auth: process.env.GITHUB_TOKEN });
  const [owner, repo] = process.env.REPO.split('/');
  const prNumber = parseInt(process.env.PR_NUMBER, 10);

  // Read the ReviewResult from core CLI
  const result = JSON.parse(fs.readFileSync(process.env.RESULT_FILE, 'utf8'));

  // Format for GitHub
  const comment = formatForGitHub(result);

  // Find existing Open Review comment or create new one
  const existingComment = await findExistingReviewComment(octokit, owner, repo, prNumber);

  if (existingComment) {
    // Update existing comment
    await octokit.issues.updateComment({
      owner,
      repo,
      comment_id: existingComment.id,
      body: comment
    });
    console.log('Review updated successfully');
  } else {
    // Create new comment
    await octokit.issues.createComment({
      owner,
      repo,
      issue_number: prNumber,
      body: comment
    });
    console.log('Review posted successfully');
  }
}

/**
 * Find an existing Open Review comment on the PR
 * Looks for comments from github-actions that contain our marker
 */
async function findExistingReviewComment(octokit, owner, repo, prNumber) {
  try {
    const { data: comments } = await octokit.issues.listComments({
      owner,
      repo,
      issue_number: prNumber,
      per_page: 100
    });

    // Find the most recent comment from github-actions with our marker
    const openReviewComments = comments
      .filter(c =>
        c.user?.login === 'github-actions' &&
        c.body?.includes(REVIEW_MARKER)
      )
      .sort((a, b) => new Date(b.created_at) - new Date(a.created_at));

    return openReviewComments[0] || null;
  } catch (error) {
    console.warn('Could not fetch existing comments:', error.message);
    return null;
  }
}

function formatForGitHub(result) {
  // Verdict mapping per TEMPLATE.md
  const verdicts = {
    approve: { icon: '✅', text: '**LGTM** — approve and merge' },
    request_changes: { icon: '⚠️', text: '**Changes requested** — do not merge' },
    comment: { icon: '⏸️', text: '**Hold** — let\'s discuss the approach' }
  };

  const v = verdicts[result.verdict] || verdicts.comment;
  let body = `${v.icon} ${v.text}\n\n`;

  // Summary (required when not approved)
  if (result.verdict !== 'approve' && result.summary) {
    body += `${result.summary}\n\n`;
  }

  // Group findings by type/severity
  const findings = result.findings || [];
  const critical = findings.filter(f => f.severity === 'critical');
  const warnings = findings.filter(f => f.severity === 'warning');
  const questions = findings.filter(f => f.type === 'question');
  const suggestions = findings.filter(f => f.type === 'suggestion' || (f.severity === 'info' && f.type !== 'question'));

  // Must Fix - Critical blocking issues
  if (critical.length > 0) {
    body += '🔴 Must fix\n\n';
    critical.forEach((f, i) => {
      body += `${i + 1}. **${f.title}**`;
      if (f.file) {
        body += ` — \`${f.file}${f.line ? ':' + f.line : ''}\``;
      }
      body += '\n';

      // Add description if present
      if (f.description) {
        body += `   ${f.description}\n`;
      }

      // Add suggested fix if present
      if (f.suggestedFix) {
        body += '\n   **Suggested fix:**\n';
        body += '   ```suggestion\n';
        body += `   ${f.suggestedFix.split('\n').join('\n   ')}\n`;
        body += '   ```\n';
      }
      body += '\n';
    });
  }

  // Should Fix - Non-blocking improvements
  if (warnings.length > 0) {
    body += '🟡 Should fix\n\n';
    warnings.forEach(f => {
      body += `- **${f.title}**`;
      if (f.file) {
        body += ` — \`${f.file}${f.line ? ':' + f.line : ''}\``;
      }
      body += '\n';

      // Add description if present
      if (f.description) {
        body += `  ${f.description}\n`;
      }

      // Add suggested fix if present
      if (f.suggestedFix) {
        body += '\n  **Suggested fix:**\n';
        body += '  ```suggestion\n';
        body += `  ${f.suggestedFix.split('\n').join('\n  ')}\n`;
        body += '  ```\n';
      }
      body += '\n';
    });
  }

  // Questions - Discussion prompts
  if (questions.length > 0) {
    body += '💬 Questions for the team\n\n';
    questions.forEach(f => {
      body += `- **${f.title}**\n`;
      if (f.description) {
        body += `  ${f.description}\n`;
      }
      body += '\n';
    });
  }

  // Suggestions - Nice-to-have (collapsed by default)
  if (suggestions.length > 0) {
    let suggestionsContent = '';
    suggestions.forEach(f => {
      suggestionsContent += `- **${f.title}**`;
      if (f.file) {
        suggestionsContent += ` — \`${f.file}${f.line ? ':' + f.line : ''}\``;
      }
      suggestionsContent += '\n';

      if (f.description) {
        suggestionsContent += `  ${f.description}\n`;
      }

      if (f.suggestedFix) {
        suggestionsContent += '\n  **Suggested fix:**\n';
        suggestionsContent += '  ```suggestion\n';
        suggestionsContent += `  ${f.suggestedFix.split('\n').join('\n  ')}\n`;
        suggestionsContent += '  ```\n';
      }
      suggestionsContent += '\n';
    });

    body += '<details>\n<summary>💡 Suggestions (non-blocking)</summary>\n\n';
    body += suggestionsContent;
    body += '</details>\n\n';
  }

  // Footer
  body += '---\n\n*Generated by Open Review*';

  return body;
}

main().catch(err => {
  console.error('Failed to post review:', err);
  process.exit(1);
});
