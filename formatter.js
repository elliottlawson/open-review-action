const REVIEW_MARKER = '*Generated by Open Review*';
const METADATA_START = '<!-- open-review:meta';
const METADATA_END = '-->';

const TIMEZONE = process.env.TIMEZONE || 'UTC';

const ICON_STOP = 'https://raw.githubusercontent.com/primer/octicons/main/icons/stop-16.svg';
const ICON_DIFF_REMOVED = 'https://raw.githubusercontent.com/primer/octicons/main/icons/diff-removed-16.svg';
const ICON_COMMENT_DISCUSSION = 'https://raw.githubusercontent.com/primer/octicons/main/icons/comment-discussion-16.svg';
const ICON_LIGHT_BULB = 'https://raw.githubusercontent.com/primer/octicons/main/icons/light-bulb-16.svg';

const ICON_CHECK_CIRCLE = 'https://raw.githubusercontent.com/primer/octicons/main/icons/check-circle-16.svg';
const ICON_ALERT = 'https://raw.githubusercontent.com/primer/octicons/main/icons/alert-16.svg';
const ICON_PAUSE = 'https://raw.githubusercontent.com/primer/octicons/main/icons/pause-16.svg';

function formatTimestamp(date = new Date()) {
  try {
    return date.toLocaleString('en-US', {
      timeZone: TIMEZONE,
      year: 'numeric',
      month: 'short',
      day: 'numeric',
      hour: 'numeric',
      minute: '2-digit',
      hour12: true,
    });
  } catch (error) {
    console.warn(`Invalid timezone '${TIMEZONE}', falling back to UTC`);
    return date.toLocaleString('en-US', {
      timeZone: 'UTC',
      year: 'numeric',
      month: 'short',
      day: 'numeric',
      hour: 'numeric',
      minute: '2-digit',
      hour12: true,
    });
  }
}

function formatMetadata(version) {
  return `${METADATA_START}\nversion: ${version}\ngenerated: ${new Date().toISOString()}\n${METADATA_END}`;
}

function formatSection({ icon, title, summary, content, isCollapsible = false }) {
  let section = `\n## <img src="${icon}" height="18" alt="">&nbsp;&nbsp;${title}\n\n`;

  if (summary) {
    section += `${summary}\n\n`;
  }

  if (isCollapsible) {
    section += '<details>\n<summary>View ' + title.toLowerCase() + '</summary>\n\n';
  }

  section += content;

  if (isCollapsible) {
    section += '\n</details>\n';
  }

  section += '---\n';

  return section;
}

function formatForGitHub(result, version = 1) {
  const verdicts = {
    approve:         { icon: ICON_CHECK_CIRCLE, text: '**LGTM** — approve and merge' },
    request_changes: { icon: ICON_ALERT,       text: '**Changes requested** — do not merge' },
    comment:         { icon: ICON_PAUSE,       text: '**Hold** — let\'s discuss the approach' }
  };

  const v = verdicts[result.verdict] || verdicts.comment;
  let body = `<img src="${v.icon}" height="20" alt="">&nbsp;&nbsp;${v.text}\n\n`;

  if (result.verdict !== 'approve' && result.summary) {
    body += `${result.summary}\n\n`;
  }

  const findings = result.findings || [];
  const critical    = findings.filter(f => f.severity === 'critical');
  const warnings    = findings.filter(f => f.severity === 'warning');
  const questions   = findings.filter(f => f.type === 'question');
  const suggestions = findings.filter(f => f.type === 'suggestion' || (f.severity === 'info' && f.type !== 'question'));

  if (critical.length > 0) {
    let content = '';
    critical.forEach((f, i) => {
      content += `${i + 1}. **${f.title}**`;
      if (f.file) content += ` — \`${f.file}${f.line ? ':' + f.line : ''}\``;
      content += '\n';
      if (f.description) content += `   ${f.description}\n`;
      if (f.suggestedFix) {
        content += '\n   **Suggested fix:**\n';
        content += '   ```suggestion\n';
        content += `   ${f.suggestedFix.split('\n').join('\n   ')}\n`;
        content += '   ```\n';
      }
      content += '\n';
    });
    body += formatSection({
      icon: ICON_STOP,
      title: 'Must fix',
      summary: result.sectionSummaries?.mustFix,
      content
    });
  }

  if (warnings.length > 0) {
    let content = '';
    warnings.forEach(f => {
      content += `- **${f.title}**`;
      if (f.file) content += ` — \`${f.file}${f.line ? ':' + f.line : ''}\``;
      content += '\n';
      if (f.description) content += `  ${f.description}\n`;
      if (f.suggestedFix) {
        content += '\n  **Suggested fix:**\n';
        content += '  ```suggestion\n';
        content += `  ${f.suggestedFix.split('\n').join('\n  ')}\n`;
        content += '  ```\n';
      }
      content += '\n';
    });
    body += formatSection({
      icon: ICON_DIFF_REMOVED,
      title: 'Should fix',
      summary: result.sectionSummaries?.shouldFix,
      content
    });
  }

  if (questions.length > 0) {
    let content = '';
    questions.forEach(f => {
      content += `- **${f.title}**\n`;
      if (f.description) content += `  ${f.description}\n`;
      content += '\n';
    });
    body += formatSection({
      icon: ICON_COMMENT_DISCUSSION,
      title: 'Questions',
      summary: result.sectionSummaries?.questions,
      content
    });
  }

  if (suggestions.length > 0) {
    let content = '';
    suggestions.forEach(f => {
      content += `- **${f.title}**`;
      if (f.file) content += ` — \`${f.file}${f.line ? ':' + f.line : ''}\``;
      content += '\n';
      if (f.description) content += `  ${f.description}\n`;
      if (f.suggestedFix) {
        content += '\n  **Suggested fix:**\n';
        content += '  ```suggestion\n';
        content += `  ${f.suggestedFix.split('\n').join('\n  ')}\n`;
        content += '  ```\n';
      }
      content += '\n';
    });
    body += formatSection({
      icon: ICON_LIGHT_BULB,
      title: 'Suggestions',
      summary: result.sectionSummaries?.suggestions,
      content,
      isCollapsible: true
    });
  }

  const timestamp = formatTimestamp();
  const metadata = formatMetadata(version);
  if (body.endsWith('---\n')) {
    body += '\n';
  } else {
    body += '---\n\n';
  }
  body += `*Generated by Open Review* · **v${version}** · ${timestamp}\n\n${metadata}`;

  return body;
}

module.exports = { formatForGitHub, formatTimestamp, formatMetadata, REVIEW_MARKER };
